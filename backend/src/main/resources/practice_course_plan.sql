-- 培养方案：专业实习、课程设计或其它实践课程
CREATE TABLE IF NOT EXISTS practice_course_plan (
    plan_id INT PRIMARY KEY AUTO_INCREMENT,
    course_code VARCHAR(20) NOT NULL COMMENT '课号',
    course_name VARCHAR(100) NOT NULL COMMENT '名称',
    semester INT NOT NULL COMMENT '学期',
    weeks INT NOT NULL COMMENT '周数',
    credit DECIMAL(3,1) NOT NULL COMMENT '学分',
    remark VARCHAR(255) COMMENT '备注'
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COMMENT='培养方案实践课程清单';

